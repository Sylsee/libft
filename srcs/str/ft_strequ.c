#include "libft.h"

int	ft_strequ(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (1);
	while (*s1 && *s2)
		if (*s1++ != *s2++)
			return (1);
	if (*s1++ != *s2++)
		return (1);
	return (0);
}
