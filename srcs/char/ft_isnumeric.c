#include "libft.h"

int	ft_isnumeric(const char *s)
{
	int	i;

	i = 0;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (ft_isdigit(s[i]))
		i++;
	return (s[i] == '\0');
}
